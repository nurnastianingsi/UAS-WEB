<center> <h3> Divonis 15 Tahun Penjara, Ini Hal yang Memberatkan Kuat Ma'ruf </h3> </center>

<p>
Hakim menilai Kuat tidak sopan di muka majelis selama persidangan. Serta kerap berbelit saat 
menyampaikan pengakuannya kepada para hakim.

"Terdakwa tidak sopan dalam persidangan. Berbelit-belit dalam persidangan sehingga menyulitkan 
jalannya persidangan, terdakwa tidak mengakui salah dan memposisikan diri sebagai orang yang tidak 
tahu dalam perkara ini. Terdakwa tidak menyesali perbuatannya," urai hakim.

Sementara itu, hal yang meringankan pada diri terdakwa Kuat Ma'ruf. "Terdakwa masih mempunyai 
tanggungan keluarga," lanjutnya.

Menanggapi hal itu, pengacara dari Kuat Ma'r

uf, Irwan Irawan merasa apa yang disampaikan hakim soal hal memberatkan itu mengada-ada. Dia pun 
tidak terima mengapa kliennya disebut tidak sopan padahal selama jalannya persidangan selalu menjalankan 
etika persidangan dengan baik.

"Ini adalah hal yang mengada-ada, klien kami dianggap tidak sopan sepanjang mengikuti persidangan," 
heran Irwan.

Karena alasan itu, Irwan mengaku siap melakukan upaya banding atas vonis 15 tahun penjara terhadap
 kliennya.
</p>